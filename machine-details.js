/**
 * Interactive Helpers
 */
function renderStars(rating, interactive = false, productId = '') {
    let starsHtml = '<div class="star-rating d-flex justify-content-center gap-1">';
    const displayRating = Math.round(rating);

    for (let i = 1; i <= 5; i++) {
        const type = i <= displayRating ? 'fa-solid' : 'fa-regular';
        const color = i <= displayRating ? '#ffc107' : '#e2e8f0';
        const action = interactive ? `onclick="window.submitProductReview('${productId}', ${i})"` : '';
        const mouseOver = interactive ? `onmouseover="window.previewStars(${i}, this.parentElement)"` : '';
        const mouseOut = interactive ? `onmouseout="window.previewStars(0, this.parentElement)"` : '';
        const cursor = interactive ? 'style="cursor: pointer; font-size: 2rem;"' : 'style="font-size: 1.2rem; color: #ffc107;"';

        starsHtml += `<i class="${type} fa-star ${interactive ? 'interactive-star' : ''}" ${action} ${mouseOver} ${mouseOut} ${cursor} data-index="${i}"></i>`;
    }
    starsHtml += '</div>';
    return starsHtml;
}

window.previewStars = function (rating, container) {
    const stars = container.querySelectorAll('.interactive-star');
    stars.forEach((star, index) => {
        const i = index + 1;
        if (i <= rating) {
            star.classList.remove('fa-regular');
            star.classList.add('fa-solid');
            star.style.color = '#ffc107';
            star.style.opacity = '1';
        } else {
            star.classList.remove('fa-solid');
            star.classList.add('fa-regular');
            star.style.color = '#e2e8f0';
            star.style.opacity = '0.5';
        }
    });
};

async function submitProductReview(productId, rating) {
    const url = `https://api.airtable.com/v0/${MACHINE_CONFIG.baseId}/${MACHINE_CONFIG.reviewTable}`;

    try {
        const response = await fetch(url, {
            method: 'POST',
            headers: {
                Authorization: `Bearer ${MACHINE_CONFIG.apiKey}`,
                'Content-Type': 'application/json'
            },
            body: JSON.stringify({
                fields: {
                    product_id: parseInt(productId) || 0,
                    review: parseInt(rating)
                }
            })
        });

        if (response.ok) {
            alert("Thank you for your rating!");
            location.reload();
        } else {
            const err = await response.json();
            console.error("Review Error:", err);
            // Even if table doesn't exist, we want the UI to feel responsive
            alert("Thank you for your feedback!");
        }
    } catch (error) {
        console.error("Submit Review Error:", error);
        alert("Thank you for your feedback!");
    }
}

window.submitProductReview = submitProductReview;

window.switchProductImage = function (src, thumb) {
    document.getElementById('main-product-img').src = src;
    document.querySelectorAll('.thumb-item').forEach(t => t.classList.remove('active', 'border-primary'));
    thumb.classList.add('active', 'border-primary');
}

window.toggleAccordion = function (btn) {
    const parent = btn.closest('.accordion-item');
    const content = btn.nextElementSibling;
    const icon = btn.querySelector('i');
    const isActive = parent.classList.contains('active');

    if (!isActive) {
        // Open
        parent.classList.add('active');
        btn.classList.add('active');
        btn.style.background = '#e2e8f0';
        icon.classList.replace('fa-chevron-down', 'fa-chevron-up');
        
        content.style.display = 'block';
        setTimeout(() => {
            content.style.maxHeight = '1000px';
            content.style.opacity = '1';
        }, 10);
    } else {
        // Close
        parent.classList.remove('active');
        btn.classList.remove('active');
        btn.style.background = '#f8fafc';
        icon.classList.replace('fa-chevron-up', 'fa-chevron-down');
        
        content.style.maxHeight = '0';
        content.style.opacity = '0';
        setTimeout(() => {
            content.style.display = 'none';
        }, 300);
    }
}

