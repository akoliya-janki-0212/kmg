/**
 * Fetch Data on Page Load
 */
document.addEventListener('DOMContentLoaded', async () => {
    // Check if this is a static page generated by the build script
    if (window.isStaticPage && window.machineData) {
        console.log("Static Page Detected: Using pre-injected data.");
        // Data is already rendered in HTML, but we might need to initialize 
        // interactive elements like Similar Machines or breadcrumbs if they were complex
        // However, the build script handled most of it.
        return;
    }

    const urlParams = new URLSearchParams(window.location.search);
    const recordId = urlParams.get('id');

    if (!recordId) {
        // Fallback for direct access to machine-details.html without ID
        const container = document.getElementById('machine-details-container');
        if (container && container.innerHTML.includes('Fetching')) {
             container.innerHTML = `
                <div class="text-center p-5">
                    <i class="fa-solid fa-triangle-exclamation fa-3x text-warning mb-3"></i>
                    <h3>Machine Not Found</h3>
                    <p>Invalid or missing Machine ID.</p>
                    <a href="machineries.html" class="btn btn-primary mt-3 px-5 rounded-pill">Back to Catalog</a>
                </div>
            `;
        }
        return;
    }

    try {
        const machine = await fetchMachineDetails(recordId);
        if (!machine) throw new Error("Machine fetch failed");

        // Resolve Main Category
        const mainCategoryName = MAIN_CAT_MAP[machine.category] || 'Machinery';

        // Fetch Sub-Category name
        const subCategoryName = await fetchSubCategoryName(machine.sub_category);

        // Fetch technical specs
        const technicalSpecs = await fetchMachineTechnical(machine.product_id || machine.id || recordId);

        // Fetch similar machines
        const similarMachines = await fetchSimilarMachines(machine.category, machine.airtableId);

        renderMachinePage(machine, mainCategoryName, subCategoryName, technicalSpecs, similarMachines);
    } catch (error) {
        console.error("Critical Render Error:", error);
        document.getElementById('machine-details-container').innerHTML = `
            <div class="text-center p-5">
                <i class="fa-solid fa-circle-exclamation fa-3x text-danger mb-3"></i>
                <h3>Error Loading Data</h3>
                <p>Unable to synchronize machine specifications. Please try again later.</p>
            </div>
        `;
    }
});

/**
 * Helper to get image URL from Airtable (String or Attachment Array)
 */
function getProductImages(record) {
    const fieldData = record.image || record.Image || record.ImageURL || record.imageurl;
    if (!fieldData) return ['assets/placeholder-machine.jpg'];
    if (Array.isArray(fieldData) && fieldData.length > 0) {
        return fieldData.map(img => img.url);
    }
    return [fieldData];
}

async function fetchMachineDetails(recordId) {
    const filterFormula = `{id} = "${recordId}"`;
    const url = `https://api.airtable.com/v0/${MACHINE_CONFIG.baseId}/${MACHINE_CONFIG.tableName}?filterByFormula=${encodeURIComponent(filterFormula)}&maxRecords=1`;
    try {
        const response = await fetch(url, {
            headers: { Authorization: `Bearer ${MACHINE_CONFIG.apiKey}` }
        });
        const data = await response.json();

        if (data.records && data.records.length > 0) {
            const record = data.records[0];
            return { airtableId: record.id, ...record.fields };
        }
        return null;
    } catch (error) {
        console.error("Machine Details Error:", error);
        return null;
    }
}

async function fetchSubCategoryName(subCategoryId) {
    if (!subCategoryId) return '';
    // If it's a numeric ID, we search by formula
    const filterFormula = `{id} = ${parseInt(subCategoryId)}`;
    const url = `https://api.airtable.com/v0/${MACHINE_CONFIG.baseId}/${MACHINE_CONFIG.categoryTable}?filterByFormula=${encodeURIComponent(filterFormula)}&maxRecords=1`;
    try {
        const response = await fetch(url, {
            headers: { Authorization: `Bearer ${MACHINE_CONFIG.apiKey}` }
        });
        const data = await response.json();
        if (data.records && data.records.length > 0) {
            return data.records[0].fields.Name || data.records[0].fields.name || '';
        }
        return '';
    } catch (error) {
        console.error("Sub-Category Fetch Error:", error);
        return '';
    }
}

async function fetchMachineTechnical(productId) {
    const numericId = parseInt(productId) || 0;
    const filterFormula = `{product_id} = ${numericId}`;
    const url = `https://api.airtable.com/v0/${MACHINE_CONFIG.baseId}/${MACHINE_CONFIG.technicalTable}?filterByFormula=${encodeURIComponent(filterFormula)}`;
    try {
        const response = await fetch(url, {
            headers: { Authorization: `Bearer ${MACHINE_CONFIG.apiKey}` }
        });
        const data = await response.json();
        return data.records.map(r => r.fields);
    } catch (error) {
        console.error("Technical Specs Error:", error);
        return [];
    }
}

async function fetchSimilarMachines(categoryId, currentId) {
    const id = Array.isArray(categoryId) ? categoryId[0] : categoryId;
    const filterFormula = `AND({category} = "${id}", RECORD_ID() != "${currentId}")`;
    const url = `https://api.airtable.com/v0/${MACHINE_CONFIG.baseId}/${MACHINE_CONFIG.tableName}?filterByFormula=${encodeURIComponent(filterFormula)}&maxRecords=4`;
    try {
        const response = await fetch(url, {
            headers: { Authorization: `Bearer ${MACHINE_CONFIG.apiKey}` }
        });
        const data = await response.json();
        return data.records.map(r => ({ id: r.id, ...r.fields }));
    } catch (error) {
        console.error("Similar Machines Error:", error);
        return [];
    }
}

